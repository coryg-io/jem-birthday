function showCard() {
   document.getElementById('card-container').style.display = "block";
   document.getElementById('show-card').style.display = "none";
   document.getElementById('bg-blur').style.display = "block";

}

function hideCard() {
   document.getElementById('card-container').style.display = "none";
   document.getElementById('show-card').style.display = "block";
   document.getElementById('bg-blur').style.display = "none";

}
